'use client'

import { createClient } from '@/lib/supabase/client'
import { getSiteUrl } from '@/lib/utils/site-url'
import { useState } from 'react'
import { trackEvent } from '@/lib/amplitude/client'

interface GoogleSignInButtonProps {
  /** The context where this button is being used */
  context: 'sign-up' | 'login'
  /** Optional className for additional styling */
  className?: string
}

export const GoogleSignInButton = ({ context, className = '' }: GoogleSignInButtonProps) => {
  const [loading, setLoading] = useState(false)
  const [error, setError] = useState<string | null>(null)

  const handleGoogleSignIn = async () => {
    setError(null)
    setLoading(true)

    // Track button click (non-blocking)
    setTimeout(() => {
      try {
        const pageRoute = typeof window !== 'undefined' ? window.location.pathname : `/auth/${context === 'sign-up' ? 'sign-up' : 'login'}`
        trackEvent('User Clicked Google Sign In Button', {
          'Button Section': context === 'sign-up' ? 'Sign Up Form' : 'Login Form',
          'Button Position': 'Social Login Section',
          'Button Text': 'Continue with Google',
          'Button Type': 'OAuth Button',
          'Button Context': context === 'sign-up' ? 'Sign up with Google' : 'Login with Google',
          'Page Route': pageRoute,
          'Auth Context': context,
        })
      } catch (err) {
        if (process.env.NODE_ENV === 'development') {
          console.warn('⚠️ Tracking error (non-blocking):', err)
        }
      }
    }, 0)

    try {
      const supabase = createClient()
      const siteUrl = getSiteUrl()
      
      const { error } = await supabase.auth.signInWithOAuth({
        provider: 'google',
        options: {
          redirectTo: `${siteUrl}/auth/callback`,
          queryParams: {
            access_type: 'offline',
            prompt: 'consent',
          },
        },
      })

      if (error) throw error
      
      // The redirect happens automatically, so we don't need to do anything else
    } catch (err) {
      const errorMessage = err instanceof Error ? err.message : 'Failed to sign in with Google'
      setError(errorMessage)
      setLoading(false)

      // Track error (non-blocking)
      setTimeout(() => {
        try {
          const pageRoute = typeof window !== 'undefined' ? window.location.pathname : `/auth/${context === 'sign-up' ? 'sign-up' : 'login'}`
          trackEvent('User Failed Google Sign In', {
            'Page Route': pageRoute,
            'Error Message': errorMessage,
            'Auth Context': context,
          })
        } catch (trackErr) {
          if (process.env.NODE_ENV === 'development') {
            console.warn('⚠️ Tracking error (non-blocking):', trackErr)
          }
        }
      }, 0)
    }
  }

  return (
    <div className={className}>
      {error && (
        <div className="mb-4 p-3 rounded-[1rem] bg-gradient-to-br from-red-200 to-orange-200 border-2 border-red-300">
          <p className="text-red-700 font-semibold text-sm">{error}</p>
        </div>
      )}
      
      <button
        type="button"
        onClick={handleGoogleSignIn}
        disabled={loading}
        className="w-full flex items-center justify-center gap-3 px-4 py-3 rounded-[1rem] border-2 border-gray-300 bg-white hover:bg-gray-50 font-semibold text-gray-700 transition-all duration-200 disabled:opacity-50 disabled:cursor-not-allowed focus:outline-none focus:ring-2 focus:ring-purple-200 focus:border-purple-300"
        aria-label={loading ? 'Signing in with Google...' : 'Continue with Google'}
        tabIndex={0}
      >
        {loading ? (
          <svg
            className="animate-spin h-5 w-5 text-gray-600"
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            aria-hidden="true"
          >
            <circle
              className="opacity-25"
              cx="12"
              cy="12"
              r="10"
              stroke="currentColor"
              strokeWidth="4"
            />
            <path
              className="opacity-75"
              fill="currentColor"
              d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"
            />
          </svg>
        ) : (
          <svg
            className="h-5 w-5"
            viewBox="0 0 24 24"
            xmlns="http://www.w3.org/2000/svg"
            aria-hidden="true"
          >
            <path
              d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
              fill="#4285F4"
            />
            <path
              d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
              fill="#34A853"
            />
            <path
              d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
              fill="#FBBC05"
            />
            <path
              d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
              fill="#EA4335"
            />
          </svg>
        )}
        <span>{loading ? 'Connecting...' : 'Continue with Google'}</span>
      </button>
    </div>
  )
}

